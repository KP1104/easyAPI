package com.iage.easyAPI.model;

public class SeriesDetailsModel {

    String seriesCd;
    String typeCd;
    String subTypeCd;
    String tranCd;

    public String getSeriesCd() {
        return seriesCd;
    }

    public void setSeriesCd(String seriesCd) {
        this.seriesCd = seriesCd;
    }

    public String getTypeCd() {
        return typeCd;
    }

    public void setTypeCd(String typeCd) {
        this.typeCd = typeCd;
    }

    public String getSubTypeCd() {
        return subTypeCd;
    }

    public void setSubTypeCd(String subTypeCd) {
        this.subTypeCd = subTypeCd;
    }

    public String getTranCd() {
        return tranCd;
    }

    public void setTranCd(String tranCd) {
        this.tranCd = tranCd;
    }
}
